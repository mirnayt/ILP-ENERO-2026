#6. Pedir un número y decir si es par o impar.

#ENTRADA
número = int(input("Ingrese un número: "))

#PROCESO
#CONDICIONALES

if número % 2 == 0:
    print("El número es par.")
elif número % 2 != 0:
    print("El número es impar.")

#SALIDA DE DATOS
print("El número es: ", número)

